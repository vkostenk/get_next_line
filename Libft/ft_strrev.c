char	*ft_strrev(char *str)
{
	int		i;
	int		l;
	char	t;

	l = 0;
	while (str[l] != '\0')
	{
		l++;
	}
	i = 0;
	l--;
	while (i < l)
	{
		t = str[i];
		str[i] = str[l];
		str[l] = t;
		l--;
		i++;
	}
	return (str);
}
